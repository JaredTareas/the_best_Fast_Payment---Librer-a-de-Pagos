    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
       
         try {
        // Obtener los datos del usuario
        double monto = Double.parseDouble(txtmonto.getText());
        String descripcion = txtdescripcion.getText();

        // Validar monto y descripci√≥n
        pay.validarMonto(monto);
        pay.validarDescripcion(descripcion);

        // Calcular el total seg√∫n el tipo de pago
        double total = 0;
        String tipoPago = boxpagos.getSelectedItem().toString();
        String descuentoInfo = "";  // Variable para mostrar el descuento
        String comisionInfo = "";   // Variable para mostrar la comisi√≥n

        switch (tipoPago) {
            case "Efectivo":
                total = pay.calcularPagoEfectivo(monto);
                // Solo se agrega el mensaje de descuento si se aplica
                if (monto > 500) {
                    descuentoInfo = "‚úÖ Descuento aplicado: 5% por monto mayor a $500\n"; // Este es solo el mensaje
                }
                break;
            case "Tarjeta":
                total = pay.calcularPagoTarjeta(monto);
                comisionInfo = "üí≥ Comisi√≥n del 3% aplicada por pago con tarjeta.\n"; // Mensaje de comisi√≥n para tarjeta
                break;
            case "Transferencia":
                total = pay.calcularPagoTransferencia(monto);
                comisionInfo = "üí∞ Comisi√≥n fija de $15 aplicada por pago con transferencia.\n"; // Mensaje de comisi√≥n para transferencia
                break;
            default:
                Arearesultado.setText("‚ùå Tipo de pago no v√°lido.");
                return;
        }

        // Calcular el IVA
        double iva = pay.calcularIVA(total);

        // Mostrar el resultado en el √°rea de texto
        String resumen = "RESUMEN DE LA TRANSACCI√ìN \n\n" +
                         "Descripci√≥n: " + descripcion + "\n" +
                         "Tipo de Pago: " + tipoPago + "\n" +
                         "Monto: $" + String.format("%.2f", monto) + "\n" +
                         descuentoInfo +  // Aqu√≠ a√±adimos solo el mensaje de descuento
                         comisionInfo +    // Aqu√≠ a√±adimos el mensaje de la comisi√≥n
                         "IVA (16%): $" + String.format("%.2f", iva) + "\n" +
                         "Total: $" + String.format("%.2f", total + iva);

        Arearesultado.setText(resumen);

    }catch (IllegalArgumentException e) {
        // En caso de que haya una excepci√≥n de validaci√≥n
        Arearesultado.setText("‚ùå Error: " + e.getMessage());
    }
        // En caso de que no se haya ingresado un monto v√°lido
        
      
        
        

    }//GEN-LAST:event_btncalcularActionPerformed

    




